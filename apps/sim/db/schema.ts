/**
 * Workflow Edges table - Block connections and data flow
 *
 * Defines the connections between workflow blocks, representing:
 * - Data flow direction (source to target)
 * - Specific connection points (handles) on blocks
 * - Execution order and dependencies
 * - Conditional routing paths
 *
 * EXECUTION MODEL:
 * - Edges determine workflow execution sequence
 * - Data flows from source outputs to target inputs
 * - Multiple edges can originate from one source
 * - Handles specify exact connection points
 *
 * HANDLE SYSTEM:
 * - sourceHandle: Output port identifier on source block
 * - targetHandle: Input port identifier on target block
 * - Enables multiple input/output connections per block
 * - Supports conditional routing and parallel execution
 *
 * PERFORMANCE:
 * - Indexes optimized for execution path queries
 * - Cascade delete maintains referential integrity
 * - Fast lookup for block dependency resolution
 */
export const workflowEdges = pgTable(
  'workflow_edges',
  {
    id: text('id').primaryKey(), // Edge unique identifier
    workflowId: text('workflow_id')
      .notNull()
      .references(() => workflow.id, { onDelete: 'cascade' }), // Parent workflow

    // Connection endpoints
    sourceBlockId: text('source_block_id')
      .notNull()
      .references(() => workflowBlocks.id, { onDelete: 'cascade' }), // Data origin block
    targetBlockId: text('target_block_id')
      .notNull()
      .references(() => workflowBlocks.id, { onDelete: 'cascade' }), // Data destination block
    sourceHandle: text('source_handle'), // Specific output port on source block
    targetHandle: text('target_handle'), // Specific input port on target block

    createdAt: timestamp('created_at').notNull().defaultNow(), // Edge creation time
  },
  (table) => ({
    // Primary index for workflow edge queries
    workflowIdIdx: index('workflow_edges_workflow_id_idx').on(table.workflowId),
    // Index for finding outgoing connections from a block
    sourceBlockIdx: index('workflow_edges_source_block_idx').on(table.sourceBlockId),
    // Index for finding incoming connections to a block
    targetBlockIdx: index('workflow_edges_target_block_idx').on(table.targetBlockId),
    // Composite index for workflow execution path analysis
    workflowSourceIdx: index('workflow_edges_workflow_source_idx').on(
      table.workflowId,
      table.sourceBlockId
    ),
    // Composite index for workflow dependency resolution
    workflowTargetIdx: index('workflow_edges_workflow_target_idx').on(
      table.workflowId,
      table.targetBlockId
    ),
  })
)

